#include <unistd.h>

void	ft_putnbr(int nb)
{
	char	res;

	if (nb == -2147483648)
		write(1, "-2147483648", 11);
	else
	{
		res = '0';
		if (nb < 0)
		{
			nb *= -1;
			write(1, "-", 1);
		}
		if ((nb / 10) > 0)
			ft_putnbr(nb / 10);
		res += (nb % 10);
		write(1, &res, 1);
	}
}
