char	*ft_strcat(char *dest, char *src)
{
	unsigned int i, j;

	i = 0;
	j = 0;
	while (dest[j])
		j++;

	while (src[i])
	{
		dest[j] = src[i];
		i++;
		j++;
	}
	return (dest);
}