int	ft_iterative_factorial(int nb)
{
	int i, f;

	if (nb < 0)
	{
		return (0);
	}
	if (nb == 0 || nb == 1)
	{
		return (1);
	}
	f = 1;
    i = 1;
	while (i <= nb)
	{
		f *= i;
		i++;
	}
	return (f);
}